static void init_lcm_registers(void)
{
	unsigned int data_array[16];
	push_table(init_setting, sizeof(init_setting) / sizeof(struct LCM_setting_table), 1);
	data_array[0] = 0x00110500;
	dsi_set_cmdq(data_array, 1, 1);
	MDELAY(120);
	data_array[0] = 0x00290500;
	dsi_set_cmdq(data_array, 1, 1);
	MDELAY(120);
}

static void lcm_init(void)
{
	vdd_enable(1);
	MDELAY(30);

#if defined(CONFIG_MFD_MT6370_PMU)
	//kzhkzh add for bias interface--5.8
	pmu_reg_write(0xB2, 0xE5);
    pmu_reg_write(0xB3, 0x24);//0x65
    pmu_reg_write(0xB4, 0x24);
    pmu_reg_write(0xB1, 0x7A);

	MDELAY(10);
#endif
	tp_reset(1);
	MDELAY(10);

	lcd_reset(1);
	MDELAY(20);
	lcd_reset(0);
	MDELAY(50);
	lcd_reset(1);

	MDELAY(120);//Must > 5ms
	init_lcm_registers();
}

static void lcm_suspend(void)
{
	unsigned int data_array[16];
	data_array[0] = 0x00280500;
	dsi_set_cmdq(data_array, 1, 1);
	MDELAY(20);
	data_array[0] = 0x00100500;
	dsi_set_cmdq(data_array, 1, 1);

	MDELAY(120);
	lcd_reset(0);
	MDELAY(50);
	tp_reset(0);
	MDELAY(50);

#if defined(CONFIG_MFD_MT6370_PMU)
	//kzhkzh add for bias interface-disable
    pmu_reg_write(0xB1, 0x36);
    MDELAY(30);
#endif
	vdd_enable(0);
	MDELAY(20);
}

static void lcm_resume(void)
{
	lcm_init();
}

